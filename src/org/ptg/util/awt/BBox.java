package org.ptg.util.awt;

public class BBox {
public int x ;
public int y;
public int r;
public int b;
public String id;

public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public int getX() {
	return x;
}
public void setX(int x) {
	this.x = x;
}
public int getY() {
	return y;
}
public void setY(int y) {
	this.y = y;
}
public int getR() {
	return r;
}
public void setR(int r) {
	this.r = r;
}
public int getB() {
	return b;
}
public void setB(int b) {
	this.b = b;
}

}
